package com.aptana.ide.editor.erb.actions;

import org.eclipse.jface.action.IMenuManager;
import org.eclipse.jface.action.Separator;
import org.eclipse.ui.IActionBars;
import org.eclipse.ui.IEditorPart;
import org.eclipse.ui.IWorkbenchActionConstants;
import org.eclipse.ui.texteditor.ITextEditor;
import org.eclipse.ui.texteditor.RetargetTextEditorAction;

import com.aptana.ide.editor.erb.IERBEditorActionConstants;
import com.aptana.ide.editors.unified.actions.UnifiedActionContributor;

/**
 * @author Chris Williams
 *
 */
public class ERBActionContributor extends UnifiedActionContributor {

	protected RetargetTextEditorAction extractPartial;
	private RetargetTextEditorAction contentAssistProposal;

	/**
	 * Default constructor.
	 */
	public ERBActionContributor() {
		super();		
		extractPartial = new RetargetTextEditorAction(EditorMessages.getResourceBundle(), "ExtractPartial.");
		extractPartial.setActionDefinitionId(IERBEditorActionConstants.EXTRACT_PARTIAL_ACTION);
		
		contentAssistProposal = new RetargetTextEditorAction(EditorMessages.getResourceBundle(), "ContentAssistProposal.");
		contentAssistProposal.setActionDefinitionId(IERBEditorActionConstants.CONTENT_ASSIST_PROPOSALS);
	}
	
	/*
	 * @see IEditorActionBarContributor#init(IActionBars)
	 */
	public void init(IActionBars bars) {
		super.init(bars);
		
		IMenuManager menuManager= bars.getMenuManager();
		IMenuManager editMenu= menuManager.findMenuUsingPath(IWorkbenchActionConstants.M_EDIT);
		if (editMenu != null) {
			editMenu.add(new Separator());
			editMenu.add(extractPartial);
		}	
	}
	
	/*
	 * @see IEditorActionBarContributor#setActiveEditor(IEditorPart)
	 */
	public void setActiveEditor(IEditorPart part) {
		super.setActiveEditor(part);

		ITextEditor editor= null;
		if (part instanceof ITextEditor)
			editor= (ITextEditor) part;

		extractPartial.setAction(getAction(editor, IERBEditorActionConstants.EXTRACT_PARTIAL_ACTION));
		contentAssistProposal.setAction(getAction(editor,IERBEditorActionConstants.CONTENT_ASSIST_PROPOSALS));
	}	
	
}
